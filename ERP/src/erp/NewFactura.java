    private void jBFinalizarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBFinalizarFacturaActionPerformed
        int filas = modelo.getRowCount();
        int col=modelo.getColumnCount();
        String[] datos= new String[col];
        
        java.util.Date dt = new java.util.Date();
        java.text.SimpleDateFormat sdf = 
        new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String currentTime = sdf.format(dt);
        
        String Query = "INSERT INTO "+"compras"+" Values("+"\""+ jTFCompraID.getText() + "\"," + "\"" + jTFIDC.getText()  + "\"," + "\"" + currentTime  + "\", " + "\"" + jTFTotal.getText() + "\")";
        ERPMain.db.insertData(Query);
        
        for(int i=1;i<=filas;i++){
            for(int j=1; j<=col;j++){
                datos[j-1]=modelo.getValueAt(i,j).toString();
            }
            Query = "INSERT INTO "+"detallescompra"+" Values("+"\""+ jTFCompraID.getText() + "\"," + "\"" + datos[0]  + "\"," + "\"" + datos[3]  + "\", " + "\"" + datos[4] + "\")";
            ERPMain.db.insertData(Query);
        }
    }//GEN-LAST:event_jBFinalizarFacturaActionPerformed

